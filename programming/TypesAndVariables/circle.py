# Calculation of the area and circumference of a circle
##

# determine radius and PI
# ... program statements here ...
radius = float(5.0)
pi = float(3.14159)

# calculate area 
# ... program statements here ...
area = pi * radius ** 2

# calculate circumference 
# ... program statements here ...
circ = 2 * pi * radius

# display results 
# ... program statements here ...
print(f"area is equal: {area}")
print(f"circumference is equal: {circ}")

